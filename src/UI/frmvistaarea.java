        con.loadingscreen();
        SwingWorker swingWorker = new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                mostrar(txtbuscar.getText());
                con.hideloading();
                return null;
            }
        };
        swingWorker.execute();
