    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        if (dateinicial.getCalendar() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fecha Inicial");
            return;
        }
        if (datefinal.getCalendar() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fecha Final");
            return;
        }

        Calendar cali, calf;
        int di, mi, yi, df, mf, yf;
        cali = dateinicial.getCalendar();
        calf = datefinal.getCalendar();
        di = cali.get(Calendar.DAY_OF_MONTH);
        df = calf.get(Calendar.DAY_OF_MONTH);
        mi = cali.get(Calendar.MONTH) + 1;
        mf = calf.get(Calendar.MONTH) + 1;
        yi = cali.get(Calendar.YEAR);
        yf = calf.get(Calendar.YEAR);

        String yeari = Integer.toString(yi);
        String mesi = Integer.toString(mi);
        String diai = Integer.toString(di);
        String horai = cbohorain.getSelectedItem().toString();
        String minutoi = cbominin.getSelectedItem().toString();
        String yearf = Integer.toString(yf);
        String mesf = Integer.toString(mf);
        String diaf = Integer.toString(df);
        String horaf = cbohorafn.getSelectedItem().toString();
        String minutof = cbominfn.getSelectedItem().toString();

        String fechaini = yeari + "-" + mesi + "-" + diai + " " + horai + ":" + minutoi + ":00";
        String fechafin = yearf + "-" + mesf + "-" + diaf + " " + horaf + ":" + minutof + ":00";

        int confirmacion = JOptionPane.showConfirmDialog(rootPane, "<html>Esta seguro de eliminar los datos de la tabla " + fsqloperations.tablaerrasebulk
                + " agregados por usted,<br> entre " + fechaini + " y " + fechafin + " ?</html>", "Confirmar", 2, JOptionPane.WARNING_MESSAGE);

        if (confirmacion == 0) {
            fsqloperations func = new fsqloperations();
            func.eliminarenbulk(fechaini, fechafin);

        }
// TODO add your handling code here:

    }//GEN-LAST:event_btnguardarActionPerformed

